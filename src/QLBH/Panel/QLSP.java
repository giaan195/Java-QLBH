    private void loadData(){
        Connection con = myFunction.getConnection();
        
        try{
        PreparedStatement pst = con.prepareStatement("select * from SANPHAM");
        ResultSet rs =  pst.executeQuery();       
        
        DefaultTableModel tbModel = (DefaultTableModel)jtbData.getModel();        
        tbModel.setRowCount(0);
        while(rs.next()){
            String MASP = rs.getString("MASP");
            String TENSP = rs.getString("TENSP");
            String DVT = rs.getString("DVT");
            String NUOCSX = rs.getString("NUOCSX");
            String GIA = myFunction.moneyCompact(rs.getString("GIA"));
            
            String data[] = {MASP,TENSP,DVT,NUOCSX,GIA};
            tbModel.addRow(data);
        }
        con.close();
        }
        catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    } 
    
