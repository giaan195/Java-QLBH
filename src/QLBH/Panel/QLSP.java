    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Connection con = myFunction.getConnection();
        try{
        String query = "select * from SANPHAM where MASP = '" + txtSearch.getText() + "' or TENSP LIKE N'%" + txtSearch.getText() + "%'" ;
        PreparedStatement pst = con.prepareStatement(query);
        ResultSet rs =  pst.executeQuery();
        
        DefaultTableModel rsModel = (DefaultTableModel)jtbData.getModel();
        
        rsModel.setRowCount(0);
        while(rs.next()){
            String MASP = rs.getString("MASP");
            String TENSP = rs.getString("TENSP");
            String DVT = rs.getString("DVT");
            String NUOCSX = rs.getString("NUOCSX");
            String GIA = myFunction.moneyCompact(rs.getString("GIA"));
            
            String data[] = {MASP,TENSP,DVT,NUOCSX,GIA};
            rsModel.addRow(data);
        }
        con.close();
        }
        // Handle any errors that may have occurred.
        catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void btnEditConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditConfirmActionPerformed
        StringBuilder sbError = new StringBuilder();
        if(txtEditMASP.getText().hashCode() == 0) sbError.append("Yêu cầu có mã Sản Phẩm! \n");
        if(txtEditMASP.getText().length()>4) sbError.append("Mã Sản Phẩm tối đa 4 kí tự, ví dụ NV01 NV02 ... \n");
       
        Connection con = myFunction.getConnection();
        if(sbError.length()>0) JOptionPane.showMessageDialog(editDialog, sbError ,"Thông Báo",HEIGHT);
        else{
            try{
                String query = "UPDATE SANPHAM SET TENSP = ?, DVT = ?, NUOCSX = ?,GIA = ? WHERE MASP = ?";
                PreparedStatement pst = con.prepareStatement(query);
                pst.setString(1, txtEditTENSP.getText());
                pst.setString(2, txtEditDVT.getText());
                pst.setString(3, txtEditNUOCSX.getText());
                pst.setString(4, txtEditGIA.getText());
                pst.setString(5, txtEditMASP.getText());

                pst.executeUpdate();
                JOptionPane.showMessageDialog(editDialog, "Ðã cập nhật thành công!");
                clear();
                con.close();
                editDialog.dispose();
            }
            // Handle any errors that may have occurred.
            catch (SQLException e) {
                JOptionPane.showMessageDialog(editDialog, e);
            }
        }
    }//GEN-LAST:event_btnEditConfirmActionPerformed


