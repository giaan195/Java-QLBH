    private void btnAddConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddConfirmActionPerformed
        StringBuilder sbError = new StringBuilder();
        if(txtAddMASP.getText().hashCode() == 0) sbError.append("Yêu cầu có mã Sản Phẩm! \n");
        if(txtAddMASP.getText().length()>4) sbError.append("Mã Sản Phẩm tối đa 4 kí tự, ví dụ NV01 NV02 ... \n");
               
        Connection con = myFunction.getConnection();
        try{
            if(sbError.length()>0) JOptionPane.showMessageDialog(addDialog, sbError ,"Thông Báo",HEIGHT);
        else{
            String query = "INSERT INTO SANPHAM VALUES (?,?,?,?,?)";
            PreparedStatement pst = con.prepareStatement(query);
            pst.setString(1, txtAddMASP.getText());
            pst.setString(2, txtAddTENSP.getText());
            pst.setString(3, txtAddDVT.getText());
            pst.setString(4, txtAddNUOCSX.getText());
            pst.setString(5, txtAddGIA.getText());

            pst.executeUpdate();
            JOptionPane.showMessageDialog(addDialog, "Ðã thêm thành công!");
            clear();
            con.close();
            addDialog.dispose();
        }
        }
        // Handle any errors that may have occurred.
        catch (SQLException e) {
            JOptionPane.showMessageDialog(addDialog, e);
        }
    }//GEN-LAST:event_btnAddConfirmActionPerformed

