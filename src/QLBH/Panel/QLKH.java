    private void loadData(){
        TableColumnModel columnModel = jtbData.getColumnModel();
        columnModel.getColumn(0).setPreferredWidth(50);
        columnModel.getColumn(1).setPreferredWidth(120);
        columnModel.getColumn(2).setPreferredWidth(230);
        columnModel.getColumn(3).setPreferredWidth(80);
        columnModel.getColumn(4).setPreferredWidth(80);
        columnModel.getColumn(5).setPreferredWidth(80);
        columnModel.getColumn(3).setPreferredWidth(80);
        columnModel.getColumn(4).setPreferredWidth(80);
        
        Connection con = myFunction.getConnection();
        
        try{
        PreparedStatement pst = con.prepareStatement("select * from KHACHHANG");
        ResultSet rs =  pst.executeQuery();       
        
        DefaultTableModel tbModel = (DefaultTableModel)jtbData.getModel();        
        tbModel.setRowCount(0);
        while(rs.next()){
            String MANV = rs.getString("MAKH");
            String HOTEN = rs.getString("HOTEN");
            String DIACHI = rs.getString("DCHI");
            String SDT = rs.getString("SODT");
            String NGSINH = myFunction.dateLongtoShort(rs.getString("NGSINH"));
            String NGDK = myFunction.dateLongtoShort(rs.getString("NGDK"));
            String DOANHSO = myFunction.moneyCompact(rs.getString("DOANHSO"));
            String LOAIKH = rs.getString("LOAIKH");
            
            String data[] = {MANV,HOTEN,DIACHI,SDT,NGSINH,NGDK,DOANHSO,LOAIKH};
            tbModel.addRow(data);
        }
        con.close();
        }
        catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    } 
    
