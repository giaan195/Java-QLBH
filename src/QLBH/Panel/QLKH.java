    private void btnAddConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddConfirmActionPerformed
        StringBuilder sbError = new StringBuilder();
        if(txtAddMAKH.getText().hashCode() == 0) sbError.append("Yêu cầu có mã Khách Hàng! \n");
        if(txtAddMAKH.getText().length()>4) sbError.append("Mã Khách Hàng tối đa 4 kí tự, ví dụ NV01 NV02 ... \n");
        if(!isNumeric(txtAddSODT.getText())&&!txtAddSODT.getText().equals("")) sbError.append("Số điện thoại phải là số! \n");
        if((!myFunction.isVNDate(txtAddNGSINH.getText())&&!txtAddNGSINH.getText().equals(""))||(!myFunction.isVNDate(txtAddNGDK.getText())&&!txtAddNGDK.getText().equals(""))) sbError.append("Ngày vào làm không đúng, hãy nhập dạng dd/MM/yyyy ex: 25/12/2020");
        
        Connection con = myFunction.getConnection();
        try{
            if(sbError.length()>0) JOptionPane.showMessageDialog(addDialog, sbError ,"Thông Báo",HEIGHT);
        else{
            String query = "INSERT INTO KHACHHANG VALUES (?,?,?,?,?,?,?,?)";
            PreparedStatement pst = con.prepareStatement(query);
            pst.setString(1, txtAddMAKH.getText());
            pst.setString(2, txtAddHOTEN.getText());
            pst.setString(3, txtAddDCHI.getText());
            pst.setString(4, txtAddSODT.getText());
            pst.setString(5, myFunction.dateShorttoLongto(txtAddNGSINH.getText()));
            pst.setString(6, myFunction.dateShorttoLongto(txtAddNGDK.getText()));
            pst.setString(7, txtAddDOANHSO.getText());
            pst.setString(8, txtAddLOAIKH.getText());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(addDialog, "Ðã thêm thành công!");
            clear();
            con.close();
            addDialog.dispose();
        }
        }
        // Handle any errors that may have occurred.
        catch (SQLException e) {
            JOptionPane.showMessageDialog(addDialog, e);
        }
    }//GEN-LAST:event_btnAddConfirmActionPerformed

