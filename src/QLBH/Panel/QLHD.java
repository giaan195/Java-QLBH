    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Connection con = myFunction.getConnection();
        try{
        String query = "select * from HOADON where SOHD = ? or MAKH = ? or MANV = ?";
        PreparedStatement pst = con.prepareStatement(query);
        if(isNumeric(txtSearch.getText())) pst.setString(1, txtSearch.getText());
        else pst.setString(1, null);
        pst.setString(2, txtSearch.getText());
        pst.setString(3, txtSearch.getText());
        
        ResultSet rs =  pst.executeQuery();
        DefaultTableModel rsModel = (DefaultTableModel)jtbData.getModel();
        rsModel.setRowCount(0);
        while(rs.next()){
            String SOHD = rs.getString("SOHD");
            String NGHD = myFunction.dateLongtoShort(rs.getString("NGHD"));
            String MAKH = rs.getString("MAKH");
            String MANV = rs.getString("MANV");
            String TRIGIA = myFunction.moneyCompact(rs.getString("TRIGIA"));
            
            String data[] = {SOHD,NGHD,MAKH,MANV,TRIGIA};
            rsModel.addRow(data);
        }
        con.close();
        }
        // Handle any errors that may have occurred.
        catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

