    private void btnAddConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddConfirmActionPerformed
        StringBuilder sbError = new StringBuilder();
        if(txtAddSOHD.getText().hashCode() == 0) sbError.append("Yêu cầu có Số Hóa Đơn! \n");
        if(txtAddSOHD.getText().length()>4) sbError.append("Số Hóa Đơn tối đa 4 kí tự, ví dụ 1001 1002 ... \n");
        if((!myFunction.isVNDate(txtAddNGHD.getText())&&!txtAddNGHD.getText().equals(""))) sbError.append("Ngày vào làm không đúng, hãy nhập dạng dd/MM/yyyy ex: 25/12/2020");
               
        Connection con = myFunction.getConnection();
        try{
            if(sbError.length()>0) JOptionPane.showMessageDialog(addDialog, sbError ,"Thông Báo",HEIGHT);
        else{
            String query;
            if(!myFunction.getID(String.valueOf(cbbAddKH.getSelectedItem())).equals("")) query = "INSERT INTO HOADON(SOHD,NGHD,MANV,TRIGIA,MAKH) VALUES (?,?,?,?,?)";
            else query = "INSERT INTO HOADON(SOHD,NGHD,MANV,TRIGIA) VALUES (?,?,?,?)";
            PreparedStatement pst = con.prepareStatement(query);
            pst.setString(1, txtAddSOHD.getText());
            pst.setString(2, myFunction.dateShorttoLongto(txtAddNGHD.getText()));
            pst.setString(3, myFunction.getID(String.valueOf(cbbAddNV.getSelectedItem())));
            pst.setString(4, txtAddGIA.getText());
            if(!myFunction.getID(String.valueOf(cbbAddKH.getSelectedItem())).equals("")) pst.setString(5, myFunction.getID(String.valueOf(cbbAddKH.getSelectedItem())));

            System.out.println(myFunction.getID(String.valueOf(cbbAddKH.getSelectedItem())));
            
            pst.executeUpdate();
            JOptionPane.showMessageDialog(addDialog, "Ðã thêm thành công!");
            clear();
            con.close();
            addDialog.dispose();
        }
        }
        // Handle any errors that may have occurred.
        catch (SQLException e) {
            JOptionPane.showMessageDialog(addDialog, e);
        }
    }//GEN-LAST:event_btnAddConfirmActionPerformed

