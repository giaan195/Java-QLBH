    private void loadData(){
        TableColumnModel columnModel = jtbData.getColumnModel();
        columnModel.getColumn(0).setPreferredWidth(40);
        columnModel.getColumn(1).setPreferredWidth(70);
        columnModel.getColumn(2).setPreferredWidth(160);
        columnModel.getColumn(3).setPreferredWidth(160);
        columnModel.getColumn(4).setPreferredWidth(100);
        
        Connection con = myFunction.getConnection();
        
        try{
        PreparedStatement pst = con.prepareStatement("select SOHD, NGHD, HD.MAKH, KH.HOTEN as TENKH, HD.MANV, NV.HOTEN as TENNV, TRIGIA from HOADON HD left join KHACHHANG KH on  HD.MAKH = KH.MAKH  left join  NHANVIEN NV on HD.MANV = NV.MANV");
        ResultSet rs =  pst.executeQuery();       
        
        DefaultTableModel tbModel = (DefaultTableModel)jtbData.getModel();        
        tbModel.setRowCount(0);
        while(rs.next()){
            String SOHD = rs.getString("SOHD");
            String NGHD = myFunction.dateLongtoShort(rs.getString("NGHD"));
            String MAKH = rs.getString("MAKH");
            String TENKH = rs.getString("TENKH");
            String KH = myFunction.mergeIdName(MAKH,TENKH);
            String MANV = rs.getString("MANV");
            String TENNV = rs.getString("TENNV");
            String NV = myFunction.mergeIdName(MANV,TENNV);
            String TRIGIA = myFunction.moneyCompact(rs.getString("TRIGIA"));
            
            String data[] = {SOHD,NGHD,KH,NV,TRIGIA};
            tbModel.addRow(data);
        }
        con.close();
        }
        catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    } 
    
