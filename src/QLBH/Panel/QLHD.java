    private void loadCBB(){
        cbbAddKH.addItem("");
        cbbEditKH.addItem("");
        Connection con = myFunction.getConnection();
        
        try{
        PreparedStatement pst = con.prepareStatement("select MAKH, HOTEN from KHACHHANG");
        ResultSet rs =  pst.executeQuery();       
        
        while(rs.next()){
            String MAKH = rs.getString("MAKH");
            String HOTEN = rs.getString("HOTEN");
            
            cbbAddKH.addItem(myFunction.mergeIdName(MAKH,HOTEN));
            cbbEditKH.addItem(myFunction.mergeIdName(MAKH,HOTEN));
        }
        
        }
        catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
        try{
        PreparedStatement pst = con.prepareStatement("select MANV, HOTEN from NHANVIEN");
        ResultSet rs =  pst.executeQuery();       
        
        while(rs.next()){
            String MANV = rs.getString("MANV");
            String HOTEN = rs.getString("HOTEN");
            
            cbbAddNV.addItem(myFunction.mergeIdName(MANV,HOTEN));
            cbbEditNV.addItem(myFunction.mergeIdName(MANV,HOTEN));
        }
        con.close();
        }
        catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
