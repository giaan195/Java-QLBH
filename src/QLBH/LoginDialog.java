    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        Connection con = myFunction.getConnection();
        try{
            String query = "select PASSWORD from NVQL where ID='" + txtUser.getText() + "'";
            PreparedStatement pst = con.prepareStatement(query);
            ResultSet rs =  pst.executeQuery();
            if(!rs.next()) JOptionPane.showMessageDialog(this, "User Không tồn tại!", "Thông Báo",JOptionPane.OK_OPTION);
            else {
                if(txtPassword.getText().equals(rs.getString("PASSWORD"))){
                    JOptionPane.showMessageDialog(this, "Đăng nhập thành công!");
                    setVisible(false);
                    id = txtUser.getText();
                }
                else
                JOptionPane.showMessageDialog(this, "Mật khẩu không đúng!", "Thông Báo",HEIGHT);
            }
            con.close();
        }
        // Handle any errors that may have occurred.
        catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

